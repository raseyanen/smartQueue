#pragma once
/**
 * ============================================================================
 *  logger.h — Кольцевой лог в RAM + вывод в Serial
 * ============================================================================
 *  - Хранит последние LOG_MAX_LINES строк в кольцевом буфере
 *  - Каждая строка до LOG_LINE_LEN символов
 *  - Метка времени (millis) автоматически
 *  - Уровни: DBG, INF, WRN, ERR
 *  - Доступ из веб-интерфейса через getLogText()
 * ============================================================================
 */

#include <Arduino.h>

// ── Настройки ────────────────────────────────────────────────────────────────
#define LOG_MAX_LINES   80      // строк в кольцевом буфере
#define LOG_LINE_LEN    120     // макс символов в строке (без \0)

// ── Уровни ───────────────────────────────────────────────────────────────────
enum LogLevel : uint8_t {
    LOG_DBG = 0,   // Debug — подробности
    LOG_INF = 1,   // Info  — штатные события
    LOG_WRN = 2,   // Warn  — нештатные, но не критические
    LOG_ERR = 3,   // Error — ошибки
};

// ── API ──────────────────────────────────────────────────────────────────────

// Инициализация (вызвать в setup до первого лога)
void log_init();

// Добавить запись (printf-формат)
void log_add(LogLevel lvl, const char* tag, const char* fmt, ...)
    __attribute__((format(printf, 3, 4)));

// Получить весь лог одной строкой (для веб-интерфейса)
// Возвращает указатель на статический буфер — использовать сразу
const char* log_get_text();

// Очистить лог
void log_clear();

// Количество записей
int log_count();

// ── Макросы-обёртки ──────────────────────────────────────────────────────────
#define LOGD(tag, fmt, ...)  log_add(LOG_DBG, tag, fmt, ##__VA_ARGS__)
#define LOGI(tag, fmt, ...)  log_add(LOG_INF, tag, fmt, ##__VA_ARGS__)
#define LOGW(tag, fmt, ...)  log_add(LOG_WRN, tag, fmt, ##__VA_ARGS__)
#define LOGE(tag, fmt, ...)  log_add(LOG_ERR, tag, fmt, ##__VA_ARGS__)